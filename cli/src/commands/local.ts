type IssueState =
  | "new_error"
  | "repro_started"
  | "repro_succeeded"
  | "repro_failed"
  | "fix_started"
  | "fix_succeeded"
  | "fix_failed"
  | "verify_started"
  | "verify_succeeded"
  | "verify_failed"
  | "deploy_started"
  | "deploy_succeeded"
  | "deploy_failed";

type ErrorLogSummary = {
  id: string;
  state: IssueState;
  receivedAt: string;
  level?: string;
  platform?: string;
  service?: {
    name?: string;
  };
  error?: {
    type?: string;
    message?: string;
  };
};

type RepairStatusResponse = {
  running: boolean;
  logId: string;
  repairAttemptId?: string;
  stage?:
    | "backend_selection"
    | "context"
    | "replicator"
    | "worker"
    | "verification"
    | "tester"
    | "promotion"
    | "complete";
  state?: IssueState;
  detail?: string;
  selectedBackend?: "docker" | "local";
  profileId?: string;
};

type TrackedRepairLog = {
  lastState: IssueState;
  completed: boolean;
  lastProgressKey: string | null;
};

const TERMINAL_ISSUE_STATES = new Set<IssueState>([
  "repro_failed",
  "fix_failed",
  "verify_succeeded",
  "verify_failed",
  "deploy_succeeded",
  "deploy_failed",
]);

